package io.datalayer.data.iterator;

public class Person {
    private String firstName;
    private String lastName;
    private String address;
    private String zipCity;
    private int zipCode;

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getZipCity() {
        return zipCity;
    }

    public void setZipCity(String zipCity) {
        this.zipCity = zipCity;
    }

    public int getZipCode() {
        return zipCode;
    }

    public void setZipCode(int zipCode) {
        this.zipCode = zipCode;
    }

    public String toString() {
        StringBuffer buffer = new StringBuffer();
        buffer.append("DatasBean[");
        buffer.append("address = ").append(address);
        buffer.append(" firstName = ").append(firstName);
        buffer.append(" lastName = ").append(lastName);
        buffer.append(" zipCity = ").append(zipCity);
        buffer.append(" zipCode = ").append(zipCode);
        buffer.append("]");
        return buffer.toString();
    }

}
