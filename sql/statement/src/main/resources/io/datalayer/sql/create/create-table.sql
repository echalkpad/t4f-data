CREATE TABLE MAIL_ACCOUNT (
	MAIL_ACCOUNT_SID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 100, INCREMENT BY 1) NOT NULL,
	MAIL_ACCOUNT_EMAIL VARCHAR ( 255 ),
	MAIL_ACCOUNT_IN_HOST VARCHAR ( 255 ) NOT NULL,
	MAIL_ACCOUNT_OUT_HOST VARCHAR ( 255 ),
	MAIL_ACCOUNT_USERNAME VARCHAR ( 255 ) NOT NULL,
	MAIL_ACCOUNT_PASSWORD VARCHAR ( 255 ) NOT NULL,
	IDENTITY_SID INTEGER NOT NULL,
	MAIL_ACCOUNT_TYPE_SID INTEGER NOT NULL
	)
;
